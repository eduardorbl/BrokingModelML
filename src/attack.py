import tensorflow as tf
import numpy as np
import matplotlib.pyplot as plt
from tensorflow.keras.datasets import cifar10
from tensorflow.keras.models import load_model
from cleverhans.tf2.attacks.fast_gradient_method import fast_gradient_method

# CIFAR-10 label mapping
class_names = ['airplane', 'automobile', 'bird', 'cat', 'deer', 
               'dog', 'frog', 'horse', 'ship', 'truck']

# Load the CIFAR-10 dataset
(_, _), (x_test, y_test) = cifar10.load_data()

# Select a random test sample (before normalization)
random_index = np.random.randint(0, x_test.shape[0])
original_sample = x_test[random_index]

# Save the original image (before normalization)
plt.imshow(original_sample)
plt.title('Original Image - No Modification')
plt.savefig('images/original.png')  # Save the original unmodified image
plt.close()

# Normalize the test sample
x_test = x_test / 255.0
sample = np.expand_dims(x_test[random_index], axis=0)

# Load the pre-trained model
model = load_model('models/cifar10_model.keras')

# Define the epsilon value (perturbation intensity)
epsilon = 0.02

# Perform the FGSM attack
adv_sample = fast_gradient_method(model, sample, epsilon, np.inf)

# Clip to ensure values are in the valid range [0, 1]
adv_sample = np.clip(adv_sample, 0, 1)

# Make predictions for the original and adversarial images
original_pred = np.argmax(model.predict(sample), axis=1)
adv_pred = np.argmax(model.predict(adv_sample), axis=1)

# Get the class names
original_class = class_names[original_pred[0]]
adv_class = class_names[adv_pred[0]]

# Display the original image (after normalization) with 'interpolation'
plt.imshow(sample[0], interpolation='bilinear')  # 'bilinear' smooths the image
plt.title(f'Original - Pred: {original_class}')
plt.savefig('images/original_smooth.png')  # Save the smoothed image
plt.close()

# Show and save the adversarial image
plt.imshow(adv_sample[0])
plt.title(f'Adversarial - Pred: {adv_class}')
plt.savefig('images/adversarial.png')  # Save adversarial image
plt.close()

print(f"Original: {original_class}")
print(f"Adversarial: {adv_class}")
